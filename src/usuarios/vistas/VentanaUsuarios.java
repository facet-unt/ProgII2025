    private void agregarListenerATabla(JTable tabla) { 
        tabla.getSelectionModel().addListSelectionListener((e) -> { 
        if (!e.getValueIsAdjusting()) {                 
            if (tabla.getSelectedRow() != -1) 
                this.filaSeleccionada = tabla.getSelectedRow(); 
            } 
        }); 
    } 
   
