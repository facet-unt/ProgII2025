    private Color base = new Color(0xE2, 0xE9, 0xE7);
    private Color base2 = new Color(0xDD, 0xE4, 0xE2);
    private Color BTN = Color.WHITE; 
    private Color TEXTO = new Color(0x21,0x42,0x3D); 
    private Color ACENTO = new Color(0x26,0xA6,0x9A);
    
    /**
     * Metodo para cambiar la vista de los componentes en la ventana
     */
    private void ajustarVista(){
        getContentPane().setBackground(new java.awt.Color(0xF1, 0xF8, 0xF6));
        panelPrincipal.setBackground(new java.awt.Color(0xE2, 0xE9, 0xE7));
        panelPrincipal.setBorder(new BevelBorder(BevelBorder.LOWERED,base.brighter(),base.brighter(),base.darker()
                                 ,base.darker()));
        panelMenu.setBackground(new Color(0xDD, 0xE4, 0xE2));
        panelMenu.setBorder(new BevelBorder(BevelBorder.LOWERED, base2.brighter(), base2.brighter(), base2.darker(),
                            base2.darker()));
        buscarCampoTexto.setCursor(new Cursor(Cursor.TEXT_CURSOR));
        buscarCampoTexto.setBorder(new BevelBorder(BevelBorder.LOWERED));
        configurarBoton(btnVolver, BTN, TEXTO, ACENTO);
        configurarBoton(btnBuscar, BTN, TEXTO, ACENTO);
        configurarBoton(btnNuevo, BTN, TEXTO, ACENTO);
        configurarBoton(btnModificar, BTN, TEXTO, ACENTO);
        configurarBoton(btnBorrar, BTN, TEXTO, ACENTO);
    }
    
    /**
     * Metodo para cambiar los colores del boton
     * @param btn
     * @param fondo
     * @param texto
     * @param acento 
     */
    private void configurarBoton(JButton btn, Color fondo, Color texto, Color acento) {
        btn.setBackground(fondo);
        btn.setForeground(texto);
        btn.setFocusPainted(false);
        btn.setBorder(new javax.swing.border.LineBorder(acento.darker(), 2, true));
        btn.setCursor(new Cursor(Cursor.HAND_CURSOR));
        btn.setOpaque(true);
    }

